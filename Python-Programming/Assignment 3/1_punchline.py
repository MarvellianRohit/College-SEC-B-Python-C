# 1. Write python program that display the String “Good Morning” but display the punch line “How are you” only when the user presses enter key.

# Print the initial greeting message "Good Morning" to the console
print("Good Morning")
# The input() function pauses the program and waits for the user to press the Enter key
input("Press Enter to continue...")
# Once Enter is pressed, print the punch line "How are you" to the console
print("How are you")
