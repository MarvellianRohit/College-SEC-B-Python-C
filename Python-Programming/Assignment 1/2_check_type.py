# Program 2: Identify data types using type() function

x = 10
y = 3.14
z = "Hello Python"
is_active = False

print("Value:", x, "Type:", type(x))
print("Value:", y, "Type:", type(y))
print("Value:", z, "Type:", type(z))
print("Value:", is_active, "Type:", type(is_active))

'''
Output:
Value: 10 Type: <class 'int'>
Value: 3.14 Type: <class 'float'>
Value: Hello Python Type: <class 'str'>
Value: False Type: <class 'bool'>
'''
